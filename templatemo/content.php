                                <p class="rty">Самый высокий пик Сибири получил своё название из-за нетающей шапки снега на вершине. Местные же называют её Уч-Сюре — «жилище трёх богов». Именно эту гору можно увидеть на гербе Республики Алтай. Известный русский путешественник и эзотерик Николай Рерих изобразил её южный склон на одной из своих картин. Он был уверен в том, что здесь скрыт вход в Шамбалу. «Владычица Алтая» — так художник называл свою могучую заснеженную музу.
                                </p>
                                <p class="rty">Второе алтайское название горы Белуха – Кадын-Бажы.  Слово «Кадын» у восточных (тюркских) народов имеет значение — «женщина высокого происхождения, уважаемая, госпожа, хозяйка», а слово «Бажы» — производное тюркского слова «Баш (Баши)» — голова, вершина. Пример: Тау-Башы (Вершина горы). Таким образом, название Кадын-Бажы можно трактовать как — «Голова Госпожи». Учитывая тенгрианское (доисламское) мировозрение восточных народов, которому присуще обожествление всей природы и гор в частности, то более широко это название можно трактовать как — «Главная гора».
                                </p>
                                <p class="rty">Белуха расположена в зоне 7-8-балльной сейсмической активности. Небольшие землетрясения здесь происходят довольно часто, что свидетельствует о постоянном росте гор. Восхождение на Белуху — не просто прогулка, это настоящая проверка на прочность: она хотя и не такая высокая, как Эльбрус, но не менее сложная. Чаще всего ее штурмуют с южной стороны — такие восхождения относятся к категории сложности 3А. Более тяжелым считается подъем по Аккемской стене (категория сложности 4А-4Б). Тут обрывы, пропасти и отвесные скалы. Но если вы преодолеете себя и путь к горе, то с вершины откроются захватывающие виды. Отсюда даже видны монгольские горы.
                                </p>
